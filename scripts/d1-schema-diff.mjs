#!/usr/bin/env node
/**
 * D1 schema drift check — CI-2 (OPERATIONAL_EXCELLENCE_REPORT.md §7):
 * "Nightly prod-schema export → CI diff vs schema_bootstrap.sql — would have
 * caught RC-B1 class before ship." RC-B1 was a live column-name mismatch
 * (scan_history.created_at vs the canonical scanned_at) that 500'd the org
 * dashboard in production while a green 1,300-test suite saw nothing, because
 * the lab schema silently disagreed with production (Production Truth Law,
 * docs/ENGINEERING_STANDARDS.md §11).
 *
 * Compares the live D1 database's actual CREATE TABLE statements against the
 * committed workers/schema_bootstrap.sql — the tracked "what production
 * should look like" reference, itself generated by scripts/lab-bootstrap-d1.mjs
 * (--dump-bootstrap) from a full replay of every migration file in production
 * order. schema_bootstrap.sql (227 tables) is the correct diff target, not the
 * older workers/schema_master.sql (216 tables, a stale v38 snapshot per
 * lab-bootstrap-d1.mjs's own header comment).
 *
 * Deliberately compares table existence and column-NAME sets per table only —
 * not types/constraints/defaults. That is the exact bug class RC-B1 was (a
 * renamed column), and staying narrow avoids noisy false positives from
 * formatting or type-annotation differences that don't affect runtime
 * behavior (operational simplicity outranks unnecessary complexity, per the
 * Standing Engineering Directive, docs/ENGINEERING_STANDARDS.md §13).
 *
 * Usage: node scripts/d1-schema-diff.mjs <live-schema.json> [reference-file] [accepted-drift-file]
 *   <live-schema.json> is the captured output of:
 *     wrangler d1 execute <db> --remote --json \
 *       --command "SELECT name, sql FROM sqlite_master WHERE type='table' ORDER BY name"
 *   [reference-file] defaults to workers/schema_bootstrap.sql
 *   [accepted-drift-file] defaults to workers/schema_drift_accepted.json
 *
 * Some reference-documents-a-column-production-doesn't-have drift is
 * deliberate, not a bug: a full code audit can confirm a column is dead (see
 * PR #74) and choose to leave production as-is rather than add unused schema.
 * Treating that as a permanent build failure trains everyone to ignore red —
 * the real failure mode this whole check exists to prevent. Each such column
 * gets one exact, reasoned entry in workers/schema_drift_accepted.json; only
 * those exact table.column pairs are excluded from hasDrift, and they still
 * print under "ACCEPTED DRIFT" so the exception stays visible, not silent.
 * This only ever applies to the reference-has-it/production-doesn't direction
 * (onlyInReference) — a column production has that the reference doesn't
 * (onlyInLive) always means the reference is stale and must be fixed there,
 * never allowlisted.
 *
 * Exit codes: 0 no drift · 1 drift found · 2 misconfigured/bad input (never
 * silently reported as "no drift" — an empty or malformed live read is a
 * monitor failure, not evidence of a clean schema).
 *
 * No dependencies; Node 18+. parseSchema/diffSchemas are exported so
 * workers/test/d1SchemaDiff.test.mjs can exercise the parser and the diff
 * logic directly against fixtures (including the real schema_bootstrap.sql).
 */
import { readFileSync } from 'node:fs';
import { resolve } from 'node:path';
import { pathToFileURL } from 'node:url';

/** Skips a single-quoted SQL string literal ('' is an escaped quote), starting just past the opening quote. Returns the index just past the closing quote. */
function skipStringLiteral(s, start) {
  let i = start;
  while (i < s.length) {
    if (s[i] === "'") {
      if (s[i + 1] === "'") {
        i += 2;
        continue;
      }
      return i + 1;
    }
    i++;
  }
  return i;
}

/** From `start` (index of an opening paren), returns the index just past its matching closing paren, treating string-literal contents as opaque. */
function skipBalancedParens(s, start) {
  let depth = 0;
  let i = start;
  while (i < s.length) {
    const ch = s[i];
    if (ch === "'") {
      i = skipStringLiteral(s, i + 1);
      continue;
    }
    if (ch === '(') {
      depth++;
      i++;
      continue;
    }
    if (ch === ')') {
      depth--;
      i++;
      if (depth === 0) return i;
      continue;
    }
    i++;
  }
  return i;
}

/** Splits a CREATE TABLE body into its top-level comma-separated clauses (column defs and table constraints), respecting nested parens and string literals. */
function splitTopLevelClauses(body) {
  const parts = [];
  let depth = 0;
  let i = 0;
  let partStart = 0;
  while (i < body.length) {
    const ch = body[i];
    if (ch === "'") {
      i = skipStringLiteral(body, i + 1);
      continue;
    }
    if (ch === '(') {
      depth++;
      i++;
      continue;
    }
    if (ch === ')') {
      depth--;
      i++;
      continue;
    }
    if (ch === ',' && depth === 0) {
      parts.push(body.slice(partStart, i));
      i++;
      partStart = i;
      continue;
    }
    i++;
  }
  parts.push(body.slice(partStart));
  return parts.map((p) => p.trim()).filter(Boolean);
}

const TABLE_CONSTRAINT_KEYWORDS = /^(PRIMARY\s+KEY|FOREIGN\s+KEY|UNIQUE|CHECK|CONSTRAINT)\b/i;

function columnNameFromClause(clause) {
  if (TABLE_CONSTRAINT_KEYWORDS.test(clause)) return null;
  const m = clause.match(/^["'`[]?(\w+)["'\]`]?/);
  return m ? m[1].toLowerCase() : null;
}

/** Parses every `CREATE TABLE [IF NOT EXISTS] name (...)` in sqlText into a Map<lowercased table name, Set<lowercased column name>>. Skips `sqlite_%`/`_cf_%` tables — SQLite/D1-internal system tables that scripts/lab-bootstrap-d1.mjs already excludes when generating schema_bootstrap.sql (its own --dump-bootstrap query filters both prefixes), so a live export that includes them must be filtered the same way or they permanently false-positive as "undocumented". */
export function parseSchema(sqlText) {
  const cleaned = sqlText.replace(/--[^\n]*/g, '');
  const tables = new Map();
  const re = /CREATE\s+TABLE\s+(?:IF\s+NOT\s+EXISTS\s+)?["'`[]?(\w+)["'\]`]?\s*\(/gi;
  let m;
  while ((m = re.exec(cleaned))) {
    const name = m[1].toLowerCase();
    const openParenIdx = re.lastIndex - 1;
    const closeIdx = skipBalancedParens(cleaned, openParenIdx);
    if (name.startsWith('sqlite_') || name.startsWith('_cf_')) {
      re.lastIndex = closeIdx;
      continue;
    }
    const body = cleaned.slice(openParenIdx + 1, closeIdx - 1);
    const columns = new Set(splitTopLevelClauses(body).map(columnNameFromClause).filter(Boolean));
    tables.set(name, columns);
    re.lastIndex = closeIdx;
  }
  return tables;
}

/** Parses the accepted-drift allowlist (workers/schema_drift_accepted.json) into
 * Map<lowercased table, Set<lowercased column>>. Every entry must carry table,
 * column, and reason — an unreasoned entry is rejected rather than silently
 * accepted, since this file exists to record audited exceptions, not to be a
 * quiet way to suppress the checker. */
export function loadAcceptedDrift(jsonText) {
  const parsed = JSON.parse(jsonText);
  const entries = Array.isArray(parsed) ? parsed : parsed.accepted;
  if (!Array.isArray(entries)) {
    throw new Error('accepted-drift file must be a JSON array, or an object with an "accepted" array');
  }
  const map = new Map();
  for (const e of entries) {
    if (!e || typeof e.table !== 'string' || typeof e.column !== 'string' || !e.reason) {
      throw new Error(`accepted-drift entry missing table/column/reason: ${JSON.stringify(e)}`);
    }
    const t = e.table.toLowerCase();
    const c = e.column.toLowerCase();
    if (!map.has(t)) map.set(t, new Set());
    map.get(t).add(c);
  }
  return map;
}

/** Diffs a live schema Map against a reference schema Map (both from parseSchema).
 * acceptedDrift (Map<table, Set<column>>, from loadAcceptedDrift) lets specific,
 * already-audited "reference has it, production doesn't" columns be excluded
 * from onlyInReference/hasDrift — they still surface in the separate
 * acceptedDrift result field for visibility, just don't fail the build. Only
 * ever filters onlyInReference; onlyInLive (production has something
 * undocumented) is never suppressed this way — that direction always means the
 * reference itself is out of date and should be fixed, not allowlisted. */
export function diffSchemas(liveTables, referenceTables, acceptedDrift = new Map()) {
  const missingInLive = [];
  const columnDrift = [];
  const acceptedDriftFound = [];

  for (const [name, refCols] of referenceTables) {
    const liveCols = liveTables.get(name);
    if (!liveCols) {
      missingInLive.push(name);
      continue;
    }
    const acceptedCols = acceptedDrift.get(name) || new Set();
    const onlyInReferenceAll = [...refCols].filter((c) => !liveCols.has(c)).sort();
    const onlyInReference = onlyInReferenceAll.filter((c) => !acceptedCols.has(c));
    const accepted = onlyInReferenceAll.filter((c) => acceptedCols.has(c));
    const onlyInLive = [...liveCols].filter((c) => !refCols.has(c)).sort();
    if (onlyInReference.length || onlyInLive.length) {
      columnDrift.push({ table: name, onlyInReference, onlyInLive });
    }
    if (accepted.length) {
      acceptedDriftFound.push({ table: name, columns: accepted });
    }
  }

  const undocumentedInLive = [...liveTables.keys()].filter((name) => !referenceTables.has(name)).sort();
  columnDrift.sort((a, b) => a.table.localeCompare(b.table));
  missingInLive.sort();
  acceptedDriftFound.sort((a, b) => a.table.localeCompare(b.table));

  return {
    missingInLive,
    undocumentedInLive,
    columnDrift,
    acceptedDrift: acceptedDriftFound,
    hasDrift: missingInLive.length > 0 || undocumentedInLive.length > 0 || columnDrift.length > 0,
  };
}

/** Unwraps `wrangler d1 execute --json` output into the concatenated `sql` text of every row. Shape verified against this repo's own established convention (tools/gen-cve-pages.mjs unwraps the identical raw[0].results). */
export function extractLiveSql(wranglerJson) {
  if (!Array.isArray(wranglerJson) || !wranglerJson[0] || !Array.isArray(wranglerJson[0].results)) {
    throw new Error('unexpected wrangler output shape: expected an array with a [0].results array');
  }
  return wranglerJson[0].results
    .map((row) => row.sql)
    .filter(Boolean)
    .join('\n\n');
}

const isMain = process.argv[1] && import.meta.url === pathToFileURL(process.argv[1]).href;

if (isMain) {
  const liveJsonPath = process.argv[2];
  const referencePath = process.argv[3] || resolve(import.meta.dirname, '../workers/schema_bootstrap.sql');
  const acceptedDriftPath = process.argv[4] || resolve(import.meta.dirname, '../workers/schema_drift_accepted.json');

  if (!liveJsonPath) {
    console.log('SCHEMA_DIFF_RESULT {"status":"CONFIG_ERROR","reason":"usage: node scripts/d1-schema-diff.mjs <live-schema.json> [reference-file] [accepted-drift-file]"}');
    process.exit(2);
  }

  let liveJson, referenceSql;
  try {
    liveJson = JSON.parse(readFileSync(liveJsonPath, 'utf8'));
    referenceSql = readFileSync(referencePath, 'utf8');
  } catch (e) {
    console.log(`SCHEMA_DIFF_RESULT ${JSON.stringify({ status: 'CONFIG_ERROR', reason: e.message })}`);
    process.exit(2);
  }

  // A missing allowlist file is fine (nothing accepted yet); a malformed one
  // is a config error, same as a malformed reference or live export — never
  // silently fall back to "no allowlist" on a file that exists but is broken.
  let acceptedDrift = new Map();
  try {
    acceptedDrift = loadAcceptedDrift(readFileSync(acceptedDriftPath, 'utf8'));
  } catch (e) {
    if (e.code !== 'ENOENT') {
      console.log(`SCHEMA_DIFF_RESULT ${JSON.stringify({ status: 'CONFIG_ERROR', reason: `accepted-drift file: ${e.message}` })}`);
      process.exit(2);
    }
  }

  let liveSql;
  try {
    liveSql = extractLiveSql(liveJson);
  } catch (e) {
    console.log(`SCHEMA_DIFF_RESULT ${JSON.stringify({ status: 'CONFIG_ERROR', reason: e.message })}`);
    process.exit(2);
  }

  const liveTables = parseSchema(liveSql);
  const referenceTables = parseSchema(referenceSql);

  if (liveTables.size === 0) {
    console.log(
      'SCHEMA_DIFF_RESULT {"status":"CONFIG_ERROR","reason":"zero tables parsed from the live export — refusing to report a false no-drift result"}'
    );
    process.exit(2);
  }

  const result = diffSchemas(liveTables, referenceTables, acceptedDrift);
  console.log(`SCHEMA_DIFF_RESULT ${JSON.stringify({ status: result.hasDrift ? 'DRIFT' : 'OK', ...result })}`);

  if (result.acceptedDrift.length) {
    console.log(`ACCEPTED DRIFT (audited, documented in ${acceptedDriftPath}, does not fail the build): ${result.acceptedDrift.length} table(s)`);
    for (const a of result.acceptedDrift) console.log(`   ${a.table}: [${a.columns.join(', ')}]`);
  }

  if (result.hasDrift) {
    console.log(
      `DRIFT DETECTED: ${result.missingInLive.length} missing table(s), ${result.undocumentedInLive.length} undocumented table(s), ${result.columnDrift.length} table(s) with column drift`
    );
    for (const t of result.missingInLive) console.log(`  MISSING IN LIVE (present in ${referencePath}, not in production): ${t}`);
    for (const t of result.undocumentedInLive) console.log(`  UNDOCUMENTED IN LIVE (in production, not in ${referencePath}): ${t}`);
    for (const d of result.columnDrift) {
      console.log(
        `  COLUMN DRIFT: ${d.table} — only in reference: [${d.onlyInReference.join(', ')}] — only in live: [${d.onlyInLive.join(', ')}]`
      );
    }
    process.exit(1);
  }

  console.log(`OK: ${liveTables.size} live tables checked against ${referenceTables.size} reference tables — no drift`);
  process.exit(0);
}
